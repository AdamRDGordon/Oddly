function translatePage(lang) {
  const map = translations[lang] || {};
  document.documentElement.lang = lang;
  document.querySelectorAll(".language-switch button").forEach(button => {
    button.classList.toggle("active", button.dataset.lang === lang);
    button.setAttribute("aria-pressed", String(button.dataset.lang === lang));
  });

  const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      return node.parentElement && !["SCRIPT", "STYLE"].includes(node.parentElement.tagName) && node.nodeValue.trim()
        ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
    }
  });
  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);
  nodes.forEach(node => {
    if (!node._oddlyEnglish) node._oddlyEnglish = node.nodeValue;
    const source = node._oddlyEnglish.trim();
    node.nodeValue = lang === "sv" && map[source]
      ? node._oddlyEnglish.replace(source, map[source])
      : node._oddlyEnglish;
  });

  document.querySelectorAll("[placeholder], [aria-label], option").forEach(el => {
    if (el.hasAttribute("placeholder")) {
      el.dataset.enPlaceholder ||= el.getAttribute("placeholder");
      el.setAttribute("placeholder", lang === "sv" ? (map[el.dataset.enPlaceholder] || el.dataset.enPlaceholder) : el.dataset.enPlaceholder);
    }
    if (el.hasAttribute("aria-label")) {
      el.dataset.enLabel ||= el.getAttribute("aria-label");
      el.setAttribute("aria-label", lang === "sv" ? (map[el.dataset.enLabel] || el.dataset.enLabel) : el.dataset.enLabel);
    }
    if (el.tagName === "OPTION") {
      el.dataset.enText ||= el.textContent;
      el.textContent = lang === "sv" ? (map[el.dataset.enText] || el.dataset.enText) : el.dataset.enText;
    }
  });
}

const nav = document.querySelector("nav");
if (nav && !nav.querySelector(".language-switch")) {
  const switcher = document.createElement("div");
  switcher.className = "language-switch";
  switcher.setAttribute("aria-label", "Language");
  switcher.innerHTML = '<button type="button" data-lang="en">EN</button><button type="button" data-lang="sv">SV</button>';
  nav.insertBefore(switcher, nav.querySelector(".nav-cta"));
  switcher.querySelectorAll("button").forEach(button => {
    button.addEventListener("click", () => {
      localStorage.setItem("oddly-language", button.dataset.lang);
      translatePage(button.dataset.lang);
    });
  });
}

if (nav && !nav.querySelector(".nav-menu-toggle")) {
  const menuToggle = document.createElement("button");
  menuToggle.type = "button";
  menuToggle.className = "nav-menu-toggle";
  menuToggle.setAttribute("aria-label", "Open menu");
  menuToggle.setAttribute("aria-expanded", "false");
  menuToggle.textContent = "☰";
  nav.appendChild(menuToggle);

  menuToggle.addEventListener("click", () => {
    const isOpen = nav.classList.toggle("menu-open");
    const isSwedish = document.documentElement.lang === "sv";
    menuToggle.setAttribute("aria-expanded", String(isOpen));
    menuToggle.setAttribute("aria-label", isOpen ? (isSwedish ? "Stäng meny" : "Close menu") : (isSwedish ? "Öppna meny" : "Open menu"));
    menuToggle.textContent = isOpen ? "×" : "☰";
  });

  nav.querySelectorAll(".nav-links a").forEach(link => {
    link.addEventListener("click", () => {
      nav.classList.remove("menu-open");
      menuToggle.setAttribute("aria-expanded", "false");
      menuToggle.setAttribute("aria-label", document.documentElement.lang === "sv" ? "Öppna meny" : "Open menu");
      menuToggle.textContent = "☰";
    });
  });
}

